# addition of two numbers
a = 30  # a value is 30
b = 40  # b value is 40
c = (a + b)  # we stored some of a+b value in c
print(c)  # printing c

# addition of two numbers by using user defined data
a = int(input("Enter a value:"))  # we need to take a value
b = int(input("Enter b value:"))  # we need to take b value
print(a + b)  # printing sum of a and b value
